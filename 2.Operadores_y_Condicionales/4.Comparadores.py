# Comparadores con numeros
# > Mayor que
print(7 > 3)
print(3 > 7)
print(7 > 7)


# < Menor que
print(5 < 6)
print(6 < 5)
print(5 < 5)

# >= Mayor o Igual
print(2 >= 1)
print(2 >= 3)
print(2 >= 2)

# <= Menor o Igual
print(1 <= 2)
print(2 <= 1)
print(2 <= 2)

# == Igualdad
print(6 == 6)
print(5 == 2)

# != Diferencia
print(6 != 10)
print(6 != 6)


